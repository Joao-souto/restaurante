DROP DATABASE IF EXISTS RESTAURANTE;

CREATE DATABASE RESTAURANTE;

USE RESTAURANTE;

-- Tabela de Filiais
CREATE TABLE FILIAIS (
    ID_FILIAL INT AUTO_INCREMENT,
    ENDERECO VARCHAR(255),
    EMAIL VARCHAR(255),
    TELEFONE VARCHAR(12),
    QUANTIDADE_MESAS INT,
    AVALIACAO DECIMAL(4,2),
    PRIMARY KEY (ID_FILIAL)
);

-- Tabela de Clientes
CREATE TABLE CLIENTES (
    ID_CLIENTE INT AUTO_INCREMENT,
    CPF VARCHAR(14) UNIQUE,
    NOME VARCHAR(255),
    SENHA VARCHAR(255),
    SEXO VARCHAR(10),
    IDADE INT,
    ENDERECO VARCHAR(255),
    EMAIL VARCHAR(255) UNIQUE,
    TELEFONE VARCHAR(12),
    DATA_CADASTRO DATETIME,
    ID_FILIAL INT,
    PRIMARY KEY (ID_CLIENTE),
    FOREIGN KEY (ID_FILIAL) REFERENCES FILIAIS(ID_FILIAL)
);

-- Tabela de Funcionários
CREATE TABLE FUNCIONARIOS (
    ID_FUNC INT AUTO_INCREMENT,
    CPF VARCHAR(14) UNIQUE,
    SENHA VARCHAR(14),
    NOME VARCHAR(255),
    SEXO VARCHAR(10),
    IDADE INT,
    ENDERECO VARCHAR(255),
    EMAIL VARCHAR(255) UNIQUE,
    TELEFONE VARCHAR(12),
    ID_FILIAL INT,
    CARGO VARCHAR(255),
    DATA_CONTRATACAO DATETIME,
    PRIMARY KEY (ID_FUNC),
    FOREIGN KEY (ID_FILIAL) REFERENCES FILIAIS(ID_FILIAL)
);

-- Tabela de Reservas
CREATE TABLE RESERVAS (
    ID_RESERVA INT AUTO_INCREMENT,
    DATA_RESERVA DATETIME,
    ID_CLIENTE INT,
    MESA VARCHAR(10),
    ID_FILIAL INT,
    CAPACIDADE INT,
    PRIMARY KEY (ID_RESERVA),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE),
    FOREIGN KEY (ID_FILIAL) REFERENCES FILIAIS(ID_FILIAL)
);

-- Tabela de Fornecedores
CREATE TABLE FORNECEDORES (
    ID_FORNECEDOR INT AUTO_INCREMENT,
    NOME VARCHAR(255),
    CNPJ VARCHAR(18) UNIQUE,
    TIPO_MATERIAL VARCHAR(255),
    EMAIL VARCHAR(255),
    TELEFONE VARCHAR(12),
    VALOR_MATERIAL DECIMAL(8,2),
    PRIMARY KEY (ID_FORNECEDOR)
);

-- Tabela de Pratos
CREATE TABLE PRATOS (
    ID_PRATO INT AUTO_INCREMENT,
    NOME VARCHAR(255),
    DESCRICAO VARCHAR(255),
    VALOR DECIMAL(5,2),
    PRIMARY KEY (ID_PRATO)
);

-- Tabela de Bebidas
CREATE TABLE BEBIDAS (
    ID_BEBIDA INT AUTO_INCREMENT,
    NOME VARCHAR(255),
    DESCRICAO VARCHAR(255),
    VALOR DECIMAL(4,2),
    PRIMARY KEY (ID_BEBIDA)
);

-- Tabela de Ingredientes
CREATE TABLE INGREDIENTES (
    ID_INGREDIENTE INT AUTO_INCREMENT,
    NOME VARCHAR(255),
    UNIDADE_MEDIDA VARCHAR(255),
    PRIMARY KEY (ID_INGREDIENTE)
);

-- Tabela de Estoque
CREATE TABLE ESTOQUE (
    ID_INGREDIENTE INT,
    ID_FORNECEDOR INT,
    QUANTIDADE INT,
    QUANTIDADE_MINIMA INT,
    ID_FILIAL INT,
    FOREIGN KEY (ID_INGREDIENTE) REFERENCES INGREDIENTES (ID_INGREDIENTE),
    FOREIGN KEY (ID_FORNECEDOR) REFERENCES FORNECEDORES(ID_FORNECEDOR),
    FOREIGN KEY (ID_FILIAL) REFERENCES FILIAIS(ID_FILIAL)
);

-- Tabela de Pedidos
CREATE TABLE PEDIDOS (
    ID_PEDIDO INT AUTO_INCREMENT,
    ID_CLIENTE INT,
    ID_PRATO INT,
    ID_BEBIDA INT,
    VALOR DECIMAL(5,2),
    TIPO_PAGAMENTO VARCHAR(255),
    ENDERECO VARCHAR(255),
    DATA_PEDIDO DATETIME,
    STATUS VARCHAR(50),
    PRIMARY KEY (ID_PEDIDO),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTES(ID_CLIENTE),
    FOREIGN KEY (ID_PRATO) REFERENCES PRATOS(ID_PRATO),
    FOREIGN KEY (ID_BEBIDA) REFERENCES BEBIDAS(ID_BEBIDA)
);

-- Tabela de Entregas
CREATE TABLE ENTREGAS (
    ID_ENTREGA INT AUTO_INCREMENT,
    ENDERECO VARCHAR(255),
    ID_PEDIDO INT,
    DATA_ENTREGA DATETIME,
    PRIMARY KEY (ID_ENTREGA),
    FOREIGN KEY (ID_PEDIDO) REFERENCES PEDIDOS(ID_PEDIDO)
);

